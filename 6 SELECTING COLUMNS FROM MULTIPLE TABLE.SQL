--NATURAL JOIN
SELECT E.FIRST_NAME,E.LAST_NAME,D.DEPARTMENT_NAME
FROM EMPLOYEES E NATURAL JOIN DEPARTMENTS D;

--SLEF JOIN
SELECT E.FIRST_NAME AS FFN,
E.LAST_NAME AS MLN
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON E.EMPLOYEE_ID=M.MANAGER_ID;

--INNER JOIN
SELECT E.FIRST_NAME,E.LAST_NAME,E.EMAIL,E.SALARY,E.COMMISSION_PCT,
E.HIRE_DATE,E.SALARY,E.JOB_ID,E.MANAGER_ID,E.DEPARTMENT_ID,E.PHONE_NUMBER,
D.DEPARTMENT_ID,D.MANAGER_ID,D.DEPARTMENT_NAME,D.LOCATION_ID
FROM EMPLOYEES E
INNER JOIN DEPARTMENTS D
ON E.DEPARTMENT_ID=D.DEPARTMENT_ID;

--LEFT JOIN
SELECT E.FIRST_NAME,E.LAST_NAME,E.EMAIL,E.SALARY,E.COMMISSION_PCT,
E.HIRE_DATE,E.SALARY,E.JOB_ID,E.MANAGER_ID,E.DEPARTMENT_ID,E.PHONE_NUMBER,
D.DEPARTMENT_ID,D.MANAGER_ID,D.DEPARTMENT_NAME,D.LOCATION_ID
FROM EMPLOYEES E
LEFT JOIN DEPARTMENTS D
ON E.DEPARTMENT_ID=D.DEPARTMENT_ID;

--LEFT JOIN
SELECT E.FIRST_NAME,E.LAST_NAME,E.EMAIL,E.SALARY,E.COMMISSION_PCT,
E.HIRE_DATE,E.SALARY,E.JOB_ID,E.MANAGER_ID,E.DEPARTMENT_ID,E.PHONE_NUMBER,
D.DEPARTMENT_ID,D.MANAGER_ID,D.DEPARTMENT_NAME,D.LOCATION_ID
FROM EMPLOYEES E
RIGHT JOIN DEPARTMENTS D
ON E.DEPARTMENT_ID=D.DEPARTMENT_ID;


--FULL JOIN
SELECT E.FIRST_NAME,E.LAST_NAME,E.EMAIL,E.SALARY,E.COMMISSION_PCT,
E.HIRE_DATE,E.SALARY,E.JOB_ID,E.MANAGER_ID,E.DEPARTMENT_ID,E.PHONE_NUMBER,
D.DEPARTMENT_ID,D.MANAGER_ID,D.DEPARTMENT_NAME,D.LOCATION_ID
FROM EMPLOYEES E
FULL JOIN DEPARTMENTS D
ON E.DEPARTMENT_ID=D.DEPARTMENT_ID;

