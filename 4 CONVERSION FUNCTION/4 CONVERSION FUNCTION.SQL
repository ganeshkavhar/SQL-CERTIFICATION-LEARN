--TO_CHAR
SELECT * FROM EMPLOYEES
WHERE TO_CHAR(HIRE_DATE,'YYYY')='2002';

SELECT EMPLOYEE_ID,SUM(SALARY)
FROM EMPLOYEES
GROUP BY EMPLOYEE_ID;


SELECT DEPARTMENT_ID
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
HAVING SUM(SALARY)>1000;

--TO NUMBER
SELECT  TO_NUMBER('121.23', '9G999D99') 
FROM DUAL;

--NULLIF IN SQL
SELECT	NULLIF (12, 12)
FROM DUAL;

--COALESCE IN SQL
SELECT	COALESCE  (12, 12)
FROM DUAL;


--DECODE IN SQL
SELECT DECODE(NULL,NULL,'EQUAL','NOT EQUAL') 
FROM DUAL;

SELECT DECODE(NULL,1,'EQUAL','NOT EQUAL') 
FROM DUAL;

SELECT DECODE(NULL,0,'EQUAL','NOT EQUAL') 
FROM DUAL;
