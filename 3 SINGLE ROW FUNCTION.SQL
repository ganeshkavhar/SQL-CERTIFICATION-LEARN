--NVL 
SELECT COMMISSION_PCT,NVL(COMMISSION_PCT,'0')
FROM EMPLOYEES;

--NVL 2
SELECT COMMISSION_PCT,NVL2(COMMISSION_PCT,'NOT APPLICABLE','0')
FROM EMPLOYEES;


--INITCAP IN SQL
SELECT INITCAP(FIRST_NAME)
FROM EMPLOYEES;

--CONCAT IN SQL
SELECT CONCAT(FIRST_NAME,LAST_NAME) AS FULLNM
FROM EMPLOYEES;

--SUBSTR IN SQL
SELECT SUBSTR(FIRST_NAME,1,4)
FROM EMPLOYEES;

--INSTR
SELECT SUBSTR (FIRST_NAME,1,5), INSTR (FIRST_NAME,'a')
FROM EMPLOYEES
WHERE ROWNUM < 5;

--LPAD AND RPAD IN SQL
SELECT RPAD(FIRST_NAME,10,'_')||LPAD (JOB_ID,15,'_')
FROM EMPLOYEES
WHERE ROWNUM < 5;

--NUMBER FUNCTION
--ROUND
SELECT ROUND (1372.66)
FROM DUAL;

SELECT ROUND (1372.34)
FROM DUAL;

--FLOOR
SELECT FLOOR(10.23)
FROM DUAL;


--ARITHMATIC OPERATORS
SELECT EMPLOYEE_ID, (SYSDATE - HIRE_DATE) EMPDATE
FROM EMPLOYEES
WHERE ROWNUM < 5;

--DATE FUNCTION
SELECT EMPLOYEE_ID, MONTHS_BETWEEN (SYSDATE, HIRE_DATE) EMP_EMP
FROM EMPLOYEES
WHERE ROWNUM < 5;
