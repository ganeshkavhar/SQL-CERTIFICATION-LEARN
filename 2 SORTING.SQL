--WHERE CLAUSE
SELECT *
FROM EMPLOYEES
WHERE EMPLOYEE_ID=100;

--COMPARISON OPERATORS
--1.LESS THAN AND GREATER THAN
SELECT *
FROM EMPLOYEES
WHERE EMPLOYEE_ID >100 AND EMPLOYEE_ID <200;

--1.LESS THAN EQUAL TO AND GREATER THAN EQUAL TO
SELECT *
FROM EMPLOYEES
WHERE EMPLOYEE_ID >=100 AND EMPLOYEE_ID <=200;

--NOT EQUALS TO 
--DIFFERENT WAY IS <> ,!=,

SELECT *
FROM EMPLOYEES
WHERE EMPLOYEE_ID !=100 AND EMPLOYEE_ID <>200;

--BETWEEN 
SELECT * FROM EMPLOYEES
WHERE EMPLOYEE_ID BETWEEN 100 AND 200;

--IN
SELECT * FROM EMPLOYEES
WHERE EMPLOYEE_ID IN(100,101,102,103,104);

--LIKE OPERATOR

SELECT * FROM EMPLOYEES
WHERE FIRST_NAME LIKE 'S%' AND FIRST_NAME LIKE '_S%' AND FIRST_NAME LIKE '__S%';

--NOT LIKE OPERATOR
SELECT * FROM EMPLOYEES
WHERE FIRST_NAME NOT LIKE 'S%' AND FIRST_NAME LIKE '_S%' AND FIRST_NAME LIKE '__S%';

--NOT NULL OPERATOR
SELECT * FROM EMPLOYEES
WHERE COMMISSION_PCT IS NOT NULL;

--LOGICAL OPERATORS

--AND 
SELECT * FROM 
EMPLOYEES
WHERE EMPLOYEE_ID=100 AND FIRST_NAME='Steven';

--OR 
SELECT * FROM 
EMPLOYEES
WHERE EMPLOYEE_ID=100 OR FIRST_NAME='JOHN';

--ORDER BY CLAUSE

--ASCENDING ORDER
SELECT FIRST_NAME,LAST_NAME,SALARY,COMMISSION_PCT FROM EMPLOYEES
ORDER BY SALARY;

--DESCENDING ORDER
SELECT FIRST_NAME,LAST_NAME,SALARY,COMMISSION_PCT FROM EMPLOYEES
ORDER BY SALARY DESC;




